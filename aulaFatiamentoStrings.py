

"""
Fatiamento de Strings, retomando.
Aqui a gente fatiou o nome Daniel. Como?

Apresentamos uma variável e a declaramos.
depois usamos chaves [] e dentro dela colocamos o comando de onde começa até onde termina
[0:3] <- significa que parará de mostrar quando chegar no índice 3, LEMBRANDO DE CONTAR O ZERO COMO NÚMERO, CABEÇÃO.
0 1 2 
"""
nome = "Daniel"
print(nome[0:3])

"""""
Aqui nós vamos fatia-la, mas de trás para frente. Isso significa que 
vamos começar fatiando do -1(L) e passará pro -2(E), assim conseguindo fazer a inversão das strings.
Caso eu queria que mostre a ultima letra do nome, só colocar [-1]
"""
nome = "Daniel"
print(nome[-2])

